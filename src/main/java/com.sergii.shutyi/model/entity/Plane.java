package com.sergii.shutyi.model.entity;

/**
 * Class represents plane entity.
 */
public class Plane {

    /**
     * ID number of the plane.
     */
    Integer id;

    /**
     * Model of the plane.
     */
    String model;

    /**
     * People capacity of the plane.
     */
    Integer peopleCapacity;

    /**
     * Carrying capacity of the plane.
     */
    Integer carryingCapacity;

    /**
     * Flight range of plane.
     */
    Integer flightRange;

    /**
     * Fuel consumption of the plane.
     */
    Integer fuelConsumption;
}
